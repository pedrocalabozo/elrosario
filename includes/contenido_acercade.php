  <h3>Historia</h3>
  



<p class="text_p">La Unidad Educativa Colegio “Nuestra Señora del Rosario” fue fundada el día 04 de septiembre de 1932, con la asistencia del gobierno del Estado Guárico y del excelentísimo Señor Obispo Monseñor Arturo Celestino Álvarez, en una casa cedida por la Diócesis, ubicada frente a la iglesia de las mercedes. Esta institución está dirigida por las hermanas dominicas de la Congregación de Santo Domingo de Granada.

Para sus comienzos el colegio contaba solo con 40 alumnos y 03 religiosas, pero debido a la calidad de la enseñanza que se impartía, la matrícula creció aceleradamente y en noviembre de ese mismo año, se hizo necesario alquilar la casa vecina.

En el año 1958, se traslada el colegio a la nueva sede, donde aún sigue funcionando; dicha sede fue construida especialmente para el funcionamiento del plantel, la misma reúne las mejores condiciones para el desarrollo normal del proceso educativo enseñanza-aprendizaje.</p>


